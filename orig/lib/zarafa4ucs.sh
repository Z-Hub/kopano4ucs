is_zarafa_server_running () {
	/usr/bin/zarafa-stats --system
	return $?
}

wait_for_zarafa_server_startup () {
	echo -n "Waiting for zarafa-server startup"
	for i in $(seq 1 10) ; do
		is_zarafa_server_running >/dev/null 2>&1 && break
		echo -n "."
		sleep 1
	done; echo
}
